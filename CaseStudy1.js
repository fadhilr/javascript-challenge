var x = 5;
var y = 8.5;

//buatlah proses perhitungan penjumlahan, pengurangan, perkalian, dan pembagian dari kedua variabel di atas.\
// penjumlahan
var jumlah = x+y;
document.write('Hasil jumlah = '+jumlah+'<br>')

//pengurangan
var kurang = x-y;
document.write('Hasil kurang = '+kurang+'<br>')

//perkalian
var kali = x*y;
document.write('Hasil kali = '+kali+'<br>')

//pembagian
var bagi = x/y;
document.write('Hasil bagi = '+bagi+'<br>')